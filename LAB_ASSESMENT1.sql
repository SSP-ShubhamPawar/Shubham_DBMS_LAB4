/*Display the total number of customers based on gender who have placed individual orders of worth at least Rs.3000.*/

SELECT COUNT(1) AS NO_CUSTOMER , CUS_GENDER FROM CUSTOMER WHERE CUS_ID IN
(SELECT DISTINCT CUS_ID FROM ORDERS WHERE ORD_AMOUNT >= 3000) GROUP BY CUS_GENDER ;

/*Display all the orders along with product name ordered by a customer having Customer_Id=2*/

SELECT ORD_ID,ORD_AMOUNT,ORD_DATE,O.PRICING_ID,P.pro_name FROM 
((ORDERS O INNER JOIN  SUPPLIER_PRICING SP ON O.PRICING_ID=SP.PRICING_ID)
INNER JOIN PRODUCT P ON P.PRO_ID = SP.PRO_ID) where O.CUS_ID=2;

/*Display the Supplier details who can supply more than one product.*/

SELECT * FROM SUPPLIER WHERE SUPP_ID IN(SELECT SUPP_iD FROM SUPPLIER_PRICING GROUP BY SUPP_iD HAVING COUNT(PRO_ID)>1);

/*Find the least expensive product from each category and print the table with category id, name, product name and price of the product*/

SELECT CA.CAT_ID, P.PRO_NAME ,CA.CAT_NAME,SP.SUPP_PRICE FROM PRODUCT P, CATEGORY CA, SUPPLIER_PRICING SP WHERE  
CA.CAT_ID = P.CAT_ID AND SP.PRO_ID = P.PRO_ID AND SP.SUPP_PRICE IN
(SELECT MIN(SP.SUPP_PRICE) as Least FROM SUPPLIER_PRICING SP ,PRODUCT P WHERE SP.PRO_ID = P.PRO_ID GROUP BY P.CAT_ID) ;

 
/*Display the Id and Name of the Product ordered after “2021-10-05”.*/

